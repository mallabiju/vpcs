namespace d_escape_characters;

class Program
{
    static void Main(string[] args)
    {
        // escape characters
        // 1. this will print "Sanothimi" in the next line.
        Console.WriteLine("Hello\nSanothimi");

        // 2. this will privide a tab between the words.
        Console.WriteLine("Hello\tBhaktapur");

        // 3. lets you to print something with double quote
        Console.WriteLine("\"Hello Sanothimi\"");
    }
}
